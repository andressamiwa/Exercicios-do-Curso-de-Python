#Soma
n1 = 10
n2 = 5

soma = n1 + n2
print('Adição = ', soma)

#sutracao

sub = n1 - n2
print('Subtração = ', sub)

#multiplicacao

mult = n1*n2
print('Multiplicação = ', mult)

#divisao
div = n1 / n2
print('Divisão = ', div)

#exponencial
exp = 2**3
print('Exponenciação = ', exp)

#divisao inteira
divint = 10 // 3
print('Divisão Inteira = ', divint)

#Como pegar o resto?
resto = 10%3
print('Resto da divisão = ', resto)